                <h2 style="font-weight: bold; " class="mt-4">Sirosis Hati</h2>
                <hr>
                <p style="text-indent: 35px;" class="mt-4">
                    Sirosis adalah kondisi ketika organ hati telah dipenuhi dengan jaringan parut dan tidak bisa berfungsi dengan normal.
                    Jaringan parut ini terbentuk akibat penyakit liver yang berkepanjangan, misalnya karena infeksi virus hepatitis atau kecanduan alkohol.
                </p>
                <p style="text-indent: 35px;">
                    Infeksi virus atau konsumsi alkohol yang berlebihan dapat mencederai hati secara perlahan. Normalnya, hati akan memperbaiki cedera tersebut dengan membentuk jaringan parut. Namun, jika kerusakan terus berlanjut atau ada kelainan pada hati, jaringan parut yang terbentuk akan makin banyak sehingga mengganggu fungsi hati.
                </p>
                <p style="text-indent: 35px;">Bila terjadi selama bertahun-tahun, sirosis bisa menyebabkan gagal hati. Karena hati adalah organ yang sangat vital bagi tubuh, gagal hati akan sangat berbahaya bagi seseorang, bahkan dapat menyebabkan kematian. Namun, jika penyebabnya diobati, perkembangan sirosis dapat dihentikan atau diperlambat.</p>

                <!-- KANKER HATI -->
                <div style="text-align: center;">
                    <img src="../img/kankerhati.jpg" alt="" width="500px">
                </div>
                <h2 style="font-weight: bold; " class="mt-4">Kanker Hati</h2>
                <hr>
                <p style="text-indent: 35px;" class="mt-4">
                    Hati memiliki banyak fungsi penting bagi tubuh, di antaranya membantu proses pencernaan makanan, mengontrol pembekuan darah, serta membersihkan darah dari racun dan zat berbahaya, seperti alkohol.
                </p>
                <p style="text-indent: 35px;">
                    Infeksi virus atau konsumsi alkohol yang berlebihan dapat mencederai hati secara perlahan. Normalnya, hati akan memperbaiki cedera tersebut dengan membentuk jaringan parut. Namun, jika kerusakan terus berlanjut atau ada kelainan pada hati, jaringan parut yang terbentuk akan makin banyak sehingga mengganggu fungsi hati.
                </p>
                <p style="text-indent: 35px;">Jika hati terserang kanker, fungsi-fungsi di atas akan terganggu sehingga racun menumpuk di dalam tubuh. Kanker hati juga akan membuat penderitanya rentan mengalami perdarahan.
                    Kanker hati adalah satu dari lima jenis kanker yang paling banyak menyebabkan kematian. Berdasarkan data Organisasi Kesehatan Dunia (WHO) pada tahun 2020, kanker hati merupakan penyebab lebih dari 800.000 kematian akibat kanker di seluruh dunia.</p>

                <h4 style="font-weight: bold;">Jenis Kanker Hati: </h4>
                <p style="text-indent: 35px;">
                    Kanker hati terbagi menjadi kanker hati primer dan kanker hati sekunder. Berikut ini adalah penjelasannya:
                </p>
                <ul>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Kanker Hati Primer</h6>
                        <p class="mx-4">Kanker hati primer adalah kanker yang memang bermula di hati. Ada beberapa jenis kanker hati primer, yaitu:</p>
                        <ol>
                            <li>
                                <em>Hepatocellular carcinoma</em>
                                <p style="text-indent: 35px;">Hepatocellular carcinoma adalah kanker hati yang bermula di sel utama yang membangun jaringan hati (sel hepatosit). Hepatocellular carcinoma merupakan jenis kanker hati primer yang paling sering terjadi, yaitu 85–90% dari seluruh kasus kanker hati.</p>
                            </li>
                            <li>
                                <em>Liver angiosarcoma</em>
                                <p style="text-indent: 35px;">Liver angiosarcoma adalah kanker hati yang bermula di sel-sel pembuluh darah di dalam hati. Angiosarcoma cenderung berkembang dengan cepat dan sering kali baru terdeteksi pada stadium lanjut.</p>
                            </li>
                            <li>
                                <em>Cholangiocarcinoma </em>
                                <p style="text-indent: 35px;">Cholangiocarcinoma adalah kanker hati yang tumbuh di sel-sel saluran empedu. Cholangiocarcinoma bisa bermula di saluran empedu yang berada di dalam hati (intrahepatic) atau di saluran empedu yang berada di luar hati (extrahepatic).</p>
                            </li>
                            <li>
                                <em>Hepatoblastoma </em>
                                <p style="text-indent: 35px;">Hepatoblastoma adalah kanker hati yang bermula dari sel hati yang belum matang. Kanker ini sangat jarang dan biasanya terjadi pada anak usia di bawah 3 tahun.</p>
                            </li>
                        </ol>
                    </li>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Kanker Hati Sekunnder</h6>
                        <p class="mx-4">Kanker hati sekunder adalah kanker yang tumbuh di organ lain, kemudian menyebar ke hati. Kanker dari organ lain yang paling sering menyebar ke hati adalah kanker lambung, kanker usus besar, kanker paru-paru, kanker kulit, dan kanker payudara.</p>
                    </li>
                </ul>


            <div class="col col-10 mt-5" style="border-right: 1px solid grey;">
                <h2 style="font-weight: bold;">Hepatitis</h2>
                <hr>
                <p style="text-indent: 35px;">
                    Hepatitis adalah peradangan pada hati atau liver. Kondisi ini bisa disebabkan oleh berbagai hal, mulai dari infeksi virus, kebiasaan mengonsumsi alkohol, penggunaan
                    obat-obatan tertentu, penyakit autoimun, dan infeksi cacing hati. Jika disebabkan oleh infeksi virus, hepatitis bisa menular ke orang lain.
                </p>
                <p style="text-indent: 35px;">Hepatitis ditandai dengan gejala berupa demam, nyeri sendi, sakit perut, dan penyakit kuning. Kondisi ini bisa berlangsung selama 6 bulan
                    (akut) atau lebih dari 6 bulan (kronis). Jika tidak ditangani dengan baik, hepatitis dapat menimbulkan komplikasi, seperti gagal hati, sirosis, hepatitis fulminan, atau kanker hati (hepatocellular carcinoma).</p>

                <h4 style="font-weight: bold;">Macam - Macam Penyakit Hepatitis</h4>
                <p style="text-indent: 35px;"> Berikut adalah penjelasan dari masing-masing penyakit hepatitis:</p>
                <ol>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Hepatitis A</h6>
                        <p class="mx-4">Infeksi hati yang sangat menular yang disebabkan Oleh virus hepatitis A. Penyakit ini Menular melalui makanan atau air yang terkontaminasi</p>
                    </li>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Hepatitis B</h6>
                        <p class="mx-4">Infeksi hati serius yang disebabkan oleh virus hepatitis B yang mudah dicegah dengan vaksin. Penyakit ini paling sering disebarkan melalui paparan cairan tubuh yang terinfeksi.</p>
                    </li>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Hepatitis C</h6>
                        <p class="mx-4">Infeksi virus yang menyerang hati dan menyebabkan peradangan.</p>
                    </li>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Hepatitis D</h6>
                        <p class="mx-4">Suatu penyakit hati serius yang disebabkan oleh infeksi virus hepatitis D. Hepatitis D hanya terjadi di kalangan orang-orang yang terinfeksi virus hepatitis B.</p>
                    </li>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Hepatitis E</h6>
                        <p class="mx-4">Penyakit hati yang disebabkan oleh virus hepatitis E. Virus hepatitis E terutama ditularkan melalui air minum yang terkontaminasi feses.</p>
                    </li>
                    <li>
                        <h6 style="font-weight: bold;" class="mx-4">Hepatitis otoimun</h6>
                        <p class="mx-4">Peradangan hati yang terjadi ketika sistem kekebalan tubuh menyerang hati. Penyebab hepatitis autoimun tidak diketahui.</p>
                    </li>
                </ol>


                <!-- SIROSIS -->
                <div style="text-align: center;">

                <h3 style="font-weight: bold; " class="mt-4">Apa Itu Serum Glutamic Pyruvic Transaminase (SGPT)?</h3>
                <p style="text-indent: 35px;" class="mt-4">
                    Serum Glutamic Pyruvic Transaminase atau SGPT merupakan salah satu enzim di dalam tubuh manusia. Enzim ini paling banyak ditemukan di dalam organ hati. SGPT juga terdapat di beberapa organ dalam jumlah yang kecil.
                    Enzim ini memiliki tugas yang cukup penting yaitu membantu mencerna protein dalam tubuh. Tes darah SGPT merupakan pemeriksaan lanjutan ketika dokter mencurigai adanya masalah pada fungsi hati.</p>
                <br>
                <h4 style="font-weight: bold;">Bagaimana Jika Hasil SGPT Tinggi?</h4>
                <p style="text-indent: 35px;">Normalnya, SGPT banyak terdapat di dalam sel-sel hati. Namun, ketika organ tersebut mengalami kerusakan, enzim ini akan keluar dari sel, kemudian masuk ke dalam pembuluh darah. Kondisi ini yang menyebabkan meningkatnya SGPT di dalam tubuh.
                    Organ hati belum tentu rusak jika kadar SGPT meningkat. Namun, sebagian besar kasusnya menandakan adanya gangguan fungsi hati.
                    Ketika hasil pemeriksaan SGPT memperlihatkan kadar yang tinggi, dokter akan melakukan tes penunjang terkait fungsi hati.
                    <br>
                    Misalnya:
                </p>

                <li class="mt-2 mx-4"><strong>Tes tingkat albumin.</strong> Ini dilakukan untuk memeriksa ketidaknormalan tingkat albumin (protein) tubuh.</li>
                <li class="mt-2 mx-4"><strong>Tes bilirubin. </strong> Ini dilakukan untuk memeriksa ketidaknormalan zat kuning dalam darah (bilirubin).</li>
                <li class="mt-2 mx-4"><strong>Tes waktu protrombin.</strong> Ini dilakukan untuk mendeteksi masalah dalam proses pembekuan darah.</li>


                <h4 style="font-weight: bold;" class="mt-3">Kapan Harus Melakukan Pemeriksaan SGPT? </h4>
                <p style="text-indent: 35px;">Pemeriksaan SGPT dilakukan ketika dokter mencurigai pasien mengalami gejala gangguan hati seperti hepatitis. Pemeriksaan penunjang ini juga dilakukan jika seseorang mengalami kondisi berikut:</p>

                <li class="mt-2 mx-4">Jaundice (sakit kuning).<br> </li>
                <li class="mt-2 mx-4">Warna urine gelap.<br> </li>
                <li class="mt-2 mx-4">Mual dan muntah.<br> </li>
                <li class="mt-2 mx-4">Nyeri di bagian perut, tepatnya pada organ hati.<br> </li>

                <p class="mt-3">Namun, pemeriksaan SGT tak hanya dilakukan jika tubuh mengalami gejala-gejala di atas saja. Pemeriksaan SGPT juga diperlukan untuk:</p>

                <li class="mt-2 mx-4">Mengevaluasi perkembangan penyakit, seperti sirosis, hepatitis, atau gangguan hati. <br> </li>
                <li class="mt-2 mx-4">Melihat perlu atau tidaknya pengobatan pada pengidap Tuberculosis (TB).<br> </li>
                <li class="mt-2 mx-4">MMengevaluasi seberapa baik perawatan kesehatan yang diberikan.<br> </li>

                <h2 style="font-weight: bold;">Apa Itu Alkaline Phosphatase?</h2>
                <p style="text-indent: 35px;">
                    Alkaline Phosphatase adalah enzim dalam aliran darah yang berfungsi memecah protein. Biasanya dihasilkan oleh organ hati dan terdapat di tulang, usus, pankreas, dan ginjal. Enzim ini penting untuk metabolisme
                    fosfat dan memecah fosfolipid dalam tubuh. Kadar Alkaline Phosphatase dalam darah dapat menunjukkan kondisi kesehatan hati dan tulang, namun harus diinterpretasikan dengan hati-hati karena dapat meningkat dalam
                    kondisi lain seperti kehamilan atau konsumsi obat-obatan tertentu.
                </p><br>
                <h4 style="font-weight: bold;">Kenapasih Kita harus tahu tentang Alkaline Phosphatase?</h4>
                <p>Karena alkaline phosphatase digunakan untuk mendeteksi adanya gangguan pada organ dalam tubuh pasien yang meliputi:</p>
                <h5 style="font-weight: bold;" class="mx-4">Hati dan Kantung Empedu</h5>
                <p class="mx-4">Memeriksa kadar ALP dalam darah adalah bagian dari tes fungsi hati dan kandung empedu. Gejala berupa penyakit kuning, sakit perut, mual, dan muntah.
                    Tes ALP dapat membantu mengidentifikasi kondisi berikut:</p>
                <ul class="mx-5">
                    <li>Penyumbatan saluran empedu.</li>
                    <li>Kolesistitis (radang kandung empedu).</li>
                    <li>Sirosis (jaringan parut pada hati).</li>
                    <li>Beberapa jenis hepatitis (radang hati).</li>
                </ul>
                <h4 style="font-weight: bold;" class="mt-3">Bagaimana Tes Level Alkaline Phosphatase Dilakukan?</h4>
                <p style="text-indent: 35px;">Tes dilakukan oleh tim medis profesional dengan mengambil sampel darah dari lengan. Tim medis akan mengingat lengan bagian atas menggunakan karet khusus agar darah menumpuk di pembuluh darah.
                    Tim medis kemudian membersihkan area lengan sebelum memasukkan jarum ke dalam vena. Setelah jarum dimasukkan, tim medis akan menarik darah dan memasukkannya ke dalam tabung kecil.</p>
                <p style="text-indent: 35px;"> Meski prosesnya cepat, prosedur biasanya menimbulkan sedikit rasa sakit dan tidak nyaman. Sampel darah yang diperoleh kemudian dianalisis di laboratorium. Hasilnya bisa memakan waktu 1 hingga 2 hari.</p>

                <h5>Bagaimana Jika Sampel Darah Menunjukkan ALP Tinggi atau Rendah?</h5>
                <li class="mt-4"><strong>Kadar ALP Tinggi</strong> <br> </li>
                <p style="text-indent: 35px;" class="mx-4">Tingginya kadar ALP mengindikasikan masalah kesehatan pada organ hati atau kantong empedu. Ini termasuk penyumbatan di saluran empedu, batu empedu, sirosis, kanker hati, dan beberapa jenis hepatitis.
                    ALP tinggi juga dikaitkan dengan gangguan pada tulang termasuk rakitis, penyakit Paget, kanker tulang, atau kelenjar paratiroid yang terlalu aktif.
                    Dalam kasus yang jarang terjadi, kadar ALP tinggi mengindikasikan gagal jantung, kanker ginjal, mononukleosis, dan infeksi bakteri.
                </p>
                <li class="mt-4"><strong>Kadar ALP Rendah</strong> <br> </li>
                <p style="text-indent: 35px;" class="mx-4">Rendahnya kadar ALP mengindikasikan pasien kekurangan protein atau mengidap penyakit Wilson (kelainan bawaan yang merusak organ hati atau otak). Ini juga menjadi pertanda pasien kekurangan gizi akibat penyakit Celiac.
                    ALP rendah juga dikaitkan dengan kondisi langka yang disebut hipofosfatemia. Ini menyebabkan tulang rapuh sehingga mudah patah.
                </p>
                <br>


